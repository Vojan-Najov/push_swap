#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dup;
	size_t	len;

	len = ft_strlen(s1) + 1;
	dup = (char *) malloc(sizeof(*dup) * len);
	if (dup)
		ft_memcpy(dup, s1, len);
	return (dup);
}
